    private void cargarDatos(){
        lstRestaurante = new RestauranteD().ConsultaRestaurantes(0, "", "");
        validarDisponibilidad("Reposteria");
        validarDisponibilidad("Italiano");
        validarDisponibilidad("Chino");
        validarDisponibilidad("Hamburguesas");
        regresar_menu = true;
        JOptionPane.showMessageDialog(this, "Seleccione un tipo de restaurante");
    }
    private void validarDisponibilidad(String tipoRestaurante) {
        Boolean habilitar = false;
        if (lstRestaurante != null && lstRestaurante.size() > 0) {
            for (Restaurante restaurante : lstRestaurante) {
                if (tipoRestaurante.equals(restaurante.getTIPO_COMIDA().toString())) {
                    ArrayList<Producto> lstProductos = new ProductoD().ConsultaProductos(restaurante.equals("") ? 0 : 2, "", restaurante.getNOMBRE());
                    if (lstProductos != null && lstProductos.size() > 0) {
                        habilitar = true;
                    }
                }
            }
        }
        if (!habilitar) {
            deshabilitarTipoComida(tipoRestaurante);
        }
    }

    private void deshabilitarTipoComida(String tipoRestaurante) {
        switch (tipoRestaurante) {
            case "Reposteria":
                btnReposteria.setEnabled(false);
                break;
            case "Italiano":
                btnItaliano.setEnabled(false);
                break;
            case "Chino":
                btnChino.setEnabled(false);
                break;
            case "Hamburguesas":
                btnHamburguesas.setEnabled(false);
                break;
        }

    }

    private void abrirCarritoCompras(String tipoComida) {
        regresar_menu = false;
        frmRestaurante ventana = new frmRestaurante(user, tipoComida);
        ventana.setVisible(true);
        ventana.setLocationRelativeTo(null);
        this.dispose();
    }

