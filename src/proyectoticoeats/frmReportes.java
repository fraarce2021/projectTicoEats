    private void btnBuscarReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarReporteActionPerformed
        limpiarTablaReporte();
        if (cboTipoReporte.getSelectedItem() == "Todos los Productos") {
            cargarReporteTodosLosProductos();
        } else if (cboTipoReporte.getSelectedItem() == "Los Tres Mejores Restaurantes") {
            cargarReporteLosTresMejoresRestaurantes();
        } else if (cboTipoReporte.getSelectedItem() == "Compras Relizadas por restaurante") {
            cargarReporteComprasRealizadasPorRestaurante();
        }else{
            tblReporte.setModel(tblModelReporte);
        }
    }//GEN-LAST:event_btnBuscarReporteActionPerformed

    private void cargarReporteTodosLosProductos() {
        tblModelReporte.addColumn("CODIGO");
        tblModelReporte.addColumn("DESCRIPCION");
        tblModelReporte.addColumn("RESTAURANTE");
        tblModelReporte.addColumn("TIPO RESTAURANTE");
        tblModelReporte.setRowCount(0);
        ArrayList<Restaurante> lstRestaurantes = new RestauranteD().ConsultaRestaurantes(0, "", "");
        for (Restaurante itemRestaurante : lstRestaurantes) {
            ArrayList<Producto> lstUsuarios = new ProductoD().ConsultaProductos(2, "", itemRestaurante.getNOMBRE());
            if (lstUsuarios != null) {
                for (Producto itemProducto : lstUsuarios) {
                    String[] fila = {itemProducto.getCODIGO(), itemProducto.getDESCRIPCION(), itemRestaurante.getNOMBRE(), itemRestaurante.getTIPO_COMIDA().name()};
                    tblModelReporte.addRow(fila);
                }
            }
            tblReporte.setModel(tblModelReporte);
        }
    }

    private void cargarReporteLosTresMejoresRestaurantes() {
        tblModelReporte.addColumn("RESTAURANTE");
        tblModelReporte.addColumn("TIPO RESTAURANTE");
        tblModelReporte.addColumn("CALIFICACION");
        
        tblModelReporte.setRowCount(0);
        ArrayList<Restaurante> lstRestaurantes = new RestauranteD().ConsultaRestaurantes(4, "", "");
        for (Restaurante itemRestaurante : lstRestaurantes) {
                    String[] fila = {itemRestaurante.getNOMBRE(), itemRestaurante.getTIPO_COMIDA().name(), itemRestaurante.getCALIFICACION()+"â˜…"};
                    tblModelReporte.addRow(fila);
            tblReporte.setModel(tblModelReporte);
        }
    }

    private void cargarReporteComprasRealizadasPorRestaurante() {
        tblModelReporte.addColumn("ORDEN");
        tblModelReporte.addColumn("CLIENTE");
        tblModelReporte.addColumn("TOTAL");
        tblModelReporte.addColumn("FECHA CREACION");
        tblModelReporte.addColumn("TIEMPO PREPARACION");
        tblModelReporte.addColumn("CALIFICACION");
        tblModelReporte.addColumn("RESTAURANTE");
        tblModelReporte.setRowCount(0);
        ArrayList<Restaurante> lstRestaurantes = new RestauranteD().ConsultaRestaurantes(0, "", "");
        for (Restaurante itemRestaurante : lstRestaurantes) {
            ArrayList<Orden> lstOrden = new OrdenD().ConsultaOrdenes(2, "", itemRestaurante.getNOMBRE());
            if (lstOrden != null) {
                for (Orden itemOrden : lstOrden) {
                    String[] fila = {Integer.toString(itemOrden.getPK_ORDEN()), itemOrden.getCLIENTE(),Double.toString(itemOrden.getPRECIO_TOTAL()),itemOrden.getFECHA_CREACION(),Integer.toString(itemOrden.getTIEMPO_PREPARACION_TOTAL()),Integer.toString(itemOrden.getCALIFICACION()), itemRestaurante.getNOMBRE()};
                    tblModelReporte.addRow(fila);
                }
            }
            tblReporte.setModel(tblModelReporte);
        }
    }

    private void limpiarTablaReporte() {
        tblModelReporte = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                //all cells false
                return false;
            }
        };
    }

